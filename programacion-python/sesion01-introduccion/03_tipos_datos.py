# Tipos numéricos

# Enteros
num_entero = 10

# Flotantes
num_flotantes = 3.1416

# Complejos (a + bj)
num_complejos = 2 + 4j

# type(variable) me dice de qué tipo es la variable
print(type(num_complejos)) # <class 'complex'>
print(type(num_flotantes)) # <class 'float'>
print(type(num_entero)) # <class 'int'>

# Strings
mi_cadena = "Anita lava la tina"

# Booleanos (verderaro o falso)
verdadero = True
falso = False

print(type(mi_cadena)) # <class 'str'>
print(type(falso)) # <class 'bool'>